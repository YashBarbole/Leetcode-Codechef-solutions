package REVISE_STRINGS;

public class String_Conca {
    public static void main(String[] args) {
        System.out.println('a'+3);
        System.out.println('a'+'b');
        System.out.println('a'-'b');
        System.out.println("a"+2);
        System.out.println((char)('a'+3));
        System.out.println("yash");

    }
}
